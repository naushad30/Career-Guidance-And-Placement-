            <h1>Automobile Engineering</h1><br><br>
            Automobile or Automotive Engineering has gained recognition and importance ever since motor vehicles capable for transporting passengers has been in vogue. <br>
            Now due to the rapid growth of auto component manufacturers and automobile industries, there is a great demand for Automobile Engineers. <br>
            Automobile Engineering alias Automotive Engineering or Vehicle Engineering is one of the most challenging careers in the field of engineering with a wide scope.<br>
            This branch deals with the designing, developing, manufacturing, testing and repairing and servicing automobiles such as cars, trucks, motorcycles, scooters etc & the related sub Engineering systems.<br>
            For the perfect blend of manufacturing and designing automobiles, Automobile Engineering uses the features of different elements of Engineering such as mechanical, electrical, electronic, software and safety engineering.<br>
            To become a proficient automobile engineer, specialized training is essential and it is a profession, which requires a lot of hard work, dedication, determination and commitment.<br>               
            The major task of an Automobile Engineer is the designing, developing, manufacturing and testing of vehicles from the concept stage to the production stage.....<br>
 
            <br><br>
<h1>What does an Automobile Engineer do?</h1><br>

•The major task of an Automobile Engineer is the designing, developing, manufacturing <br> 
•Testing of vehicles from the concept stage to the production stage.<br>
<br>

<h1>What is the future for an Automobile Engineer?</h1>
 <br>
This field offers wide range of opportunities for the aspirants and it includes automobile manufacturing industries, production plants, service stations, state road transport corporations, private transport companies, insurance companies, motor vehicle departments etc.<br>
With the computer knowledge in CAD (Computer Aided design), CAM (Automation), ERP, Automobile engineers can find employment in computer aided industries as Designers or <br>
they can design models of vehicles by joining the Research and Development wing of companies.<br>
Diploma holders in Automobile engineering can start their own business enterprise like automobile maintenance workshop or automobile garages. <br>
Automobile maintenance Engineers are offered the post of general supervision of mechanics in a workshop, factory or garage.<br>
B. tech holders in automobile engineering with an MBA degree could reach the frontline in marketing management careers.<br> 
 


 <h1>Career Prospects</h1>
 
