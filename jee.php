   <center><h3>Joint Entrance Examination</h3></center>
   <br>
   <pre>
    Joint Entrance Examination (JEE) is an engineering entrance examination conducted for admission to various engineering colleges in India. 
   It is constituted by two different examinations - JEE Main and the JEE Advanced. The exams are of the objective pattern. 
   JEE Advanced is regarded internationally as one of the most challenging undergraduate admission tests.
     <br>
    <center> <h3>JEE MAIN</h3></center>
     JEE Main has two papers, Paper-1 and Paper-2. 
     Candidates may opt for either or both of them. Both papers contain multiple choice questions. 
     Paper 1 is for admission to B.E./B.Tech courses and is conducted only online. 
     Paper-2 is for admission in B.Arch and B.Planning courses and is conducted offline only.

    JEE Main, unlike JEE Advanced, has a fixed exam structure and is not subject to change every year.
    Paper-1 is of three hours duration and consists of thirty multiple-choice (single-correct) questions in each of the three subjects (physics, chemistry, and maths). 
    There is negative marking for incorrect answers. 4 marks are awarded for correct answers and 1 mark is deducted for incorrect answers

    <center> <h3>JEE ADVANCE</h3></center>
    The exam carried a total of 372 marks with each paper carrying 186 marks. 
    The level of difficulty of questions was increased to a larger extent. 
    Moreover, number of integer type questions (having no negative marking) were reduced. 
    The paper was set up in collaboration of different IITs with the principal organiser being the IIT Guwahati.
    The Physics paper was set by IIT Madras, Mathematics by IIT Guwahati and the Chemistry portion was jointly set by IIT Bombay and IIT Delhi. 
    These features made this paper the toughest ever JEE Paper until date.

    The paper had a huge surprise of partial marking in multiple correct choices where if a student marked some but not all correct answers without marking the wrong ones, he/she would be awarded one mark for each correct option


