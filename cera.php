<h1>Some job titles</h1>
<br>
•Ceramic Engineers deals with the study of the properties, manufacture, design and applications of ceramic materials.<br>
 Ceramic engineers are involved in any of the following areas of work:<br>
 
 Research : Ceramic technologists use their scientific knowledge to anticipate new applications to replace existing products.<br> 
 Research work involves the study and application of chemical and thermal interactions of oxides which make ceramics.<br>
 
  
 Design : Ceramic engineers modify the design of the machinery and tools used in the production process and also work on the design of ceramic products.<br>
 
  
 Production: Since ceramic has such diverse uses the ceramic technologists generally specialise in the technological developments of specific production processes.<br>
 Ceramic technologists may specialise in glass working in industries engaged in manufacture of tableware, fibre optics, bulbs, window panes, electronics ancillaries. Specialist in structural clay work in the manufacture of enamelled articles and pipes, engine pars, tools artificial limbs or cement used in construction or ceramic wares such as tiles, pottery and bathroom and kitchen fixtures.
 Highly demanding specialisation is for the electronics sector for manufacturing integrated circuits, sensors etc.<br>
  
 Testing : The samples prepared for production are tested for color, surface finish, texture, strength and uniformity and for the and for the correction of the manufacturing processes by engineers in testing laboratories. Even the quality of raw materials used in the industry is tested.<br>
  
 

 
   
               
