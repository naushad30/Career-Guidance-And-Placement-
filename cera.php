            <h1>Ceramic technology </h1><br><br>
            Ceramic Technology is an emerging branch of Engineering/Technology dealing with the study of the properties, manufacture, design and applications of ceramic materials.<br> 
            The ceramic designers are concerned with designing various types of ceramic wares used in industries, households, institutions, corporate houses and other places. <br>
            The use of ceramic ware can be for both decorative as well as functional purposes.<br>
            Ceramic products include table and kitchenware, decorative ware like flower vase, insulators, potters, building materials like tiles, etc.<br>
            Cement is also considered as one of the ceramic materials.<br>
            A ceramic technologist is concerned with study, research and development of the ceramic materials, planning and developing processing systems, designing and construction of plants and machinery along with other engineers, planning and implementation of processing methodologies, designs and development of various applications like glass, porcelain, cement, refractors, insulators, composite materials, enamels, cement etc.<>
 

