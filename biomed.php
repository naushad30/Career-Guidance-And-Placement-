            <h1>Biomedical Engineering</h1><br><br>
            Biomedical Engineering alias BME is an emerging and exciting discipline of engineering that has got tremendous potential for research, development and employment. <br>
            Biomedical engineering may be defined as the study and application of engineering principles and techniques to the medical field. It is a combination of technical knowledge in biology and medicine to improve life quality.<br> 
            It is an apt career for those who have a keen interest in medicine and healthcare.<br>           
            BME is concerned with the development and manufacture of prostheses, medical devices, diagnostic devices, drugs and other therapies. This unique field encompasses bio-instrumentation, bio-materials, bio-mechanics, medical imaging, genetic engineering, orthopedic surgery, cellular and tissue engineering.<br>
            the combination of Mechanical Engineering, Electronics Engineering, Sciences with Computers for communication and control led to the emergence of interdisciplinary courses such as Mechatronics, Computer Integrated Manufacturing (CIM), Geographical Information System (GIS), Bio Technology, Bio Mechanics, Bio Medical Instrumentation and Bio Medical Engineering.<br>
 

            <br><br>
<h1>What does an Biomedical Engineer do?</h1><br>
 
These professionals are termed as bio-engineers or biomedical engineers.<br> 
A Biomedical engineer has to be an expert in engineering sciences, biological sciences and medical sciences.<br>
They must be capable of defining a medical problem in engineering terms and find a solution that satisfies both engineering and clinical requirements. They usually design and develop devices and systems ranging from cardiac monitors to clinical computers, artificial hearts to contact lenses, wheel chairs to artificial tendons.<br> 
They are also integral in the management of technology in hospitals and health care delivery.<br>
 

