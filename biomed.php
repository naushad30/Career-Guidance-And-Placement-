 <h1>Career Prospects</h1>
 
 Bio-medical engineers and technologists can find job openings in a wide range of global industries which may include modern health care, hospitals, medical equipment manufacturing units, industrial firms, educational and medical institutions, pharmaceuticals, government regulatory agencies and so on.<br>
 In hospitals, they have to work closely with other healthcare professionals like physicians, nurses, therapists and technicians.<br> 
 They develop customised devices and provide advice for selecting, handling and maintaining medical equipments. In industries, Biomedical Engineers use their understanding of living systems and technology to design and test new products.<br> 
 Government positions often involve product testing as well as establishing safety standards for devices.<br>
