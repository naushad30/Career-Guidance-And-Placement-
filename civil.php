            <h1>Civil Engineering</h1><br>
           <br> Civil Engineering is one of the broadest and oldest of the engineering disciplines, extenrding across many technical specialties. 
           <br>Civil Engineers plan, design, and supervise the construction of facilities essential to modern life like space satellites and launching facilities, offshore structures, bridges, buildings, tunnels, highways, transit systems, dams, airports, harbors, water supply system and wastewater treatment plants. 
           <br>A civil engineer is responsible for planning and designing a project, constructing the project to the required scale, and maintenance of the project.
           <br> A civil engineer requires not only a high standard of engineering knowledge but also supervisory and administrative skills.
<h1>What does an Civil Engineer do?</h1><br>
 
•	Civil engineers are involved in the design and building of highways, bridges and tunnels, schools, hospitals, airports and other buildings, sewage systems and water treatment facilities.<br>
•	Most structures, large and small, require the help of a civil engineer whether in the designing, planning or managing of the project.<br>
•	Civil engineers also help to preserve our environment by assisting in the cleaning up of existing pollution and planning ways to reduce future pollution of our air, land and water.<br>

<br>

