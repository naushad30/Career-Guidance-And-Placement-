            <h1>Agricultural Engineering</h1><br><br>
            When the principles of engineering and biological science are applied to agriculture, it is called agricultural engineering.<br> 
            Agricultural engineering, sometimes known as biological engineering, is a diverse engineering discipline, which mainly focus on the creation and development of tools (i.e farm machineries and other implements related to agriculture) that can improve farming techniques.<br>
            It can also be defined as a field of agricultural science where engineering principles are applied for the improvement and advancement of agriculture industry.<br>
            Modern agriculture is highly competitive and technology-driven with a wide variety of specialties and techniques.<br>
            Hi-tech agricultural graduates are in great demand due to the global trend toward standardization of all agricultural products and equipment. <br>
            In recent years, agricultural engineering has contributed a lot to the agricultural production in the country both directly and indirectly.<br>
            In agricultural engineering, the knowledge of agricultural principles are combined with the principles of animal biology, plant biology, mechanical engineering, civil engineering and chemical engineering.<br>
            At the same time, it also utilises mathematics and science principles for the practical solutions to agricultural issues. Compared with the other engineering disciplines, agricultural engineering has an extensive scope.<br>
            It is a process which involves production, processing, transportation and storage of agricultural commodities and reconstruction of rural areas.<br>
            The four main branches of Agricultural engineering are (i) farm equipments (ii) rural structures (iii) soil conservation, drainage, and irrigation and (iv) rural electricity.<br>
            A career in the field of agricultural engineering is quite interesting, challenging as well as rewarding. It is an apt career for those who have keen agricultural knowledge along with good engineering skills. <br>
            Agricultural engineers are those who design agricultural equipment and agricultural structures by means of better engineering methods, inventions and new technologies.<br>
            In other words, they have to look after the agricultural functioning to bring about efficient increase in food productivity. <br>
            With their engineering skills and knowledge, agricultural engineers solve problems related to agricultural equipment, water quality and water management, biological products, livestock facilities, food processing, and many other agricultural areas.<br>

