            <h1>Mechanical engineering</h1><br><br>
            Mechanical engineering is a discipline of engineering that applies the principles of physics and materials science for analysis, design, manufacturing, and maintenance of mechanical systems.<br>
            It is the branch of engineering that involves the production and usage of heat and mechanical power for the design, production, and operation of machines and tools.<br>
            It is one of the oldest and broadest engineering disciplines.
            The engineering field requires an understanding of core concepts including mechanics, kinematics, thermodynamics, materials science, and structural analysis. <br>
            Mechanical engineers use these core principles along with tools like computer-aided engineering and product lifecycle management to design and analyze manufacturing plants, industrial equipment and machinery, heating and cooling systems, transport systems, aircraft, watercraft, robotics, medical devices and more.<br>
            Mechanical Engineering science emerged in the 19th century as a result of developments in the field of physics. The field has continually evolved to incorporate advancements in technology. Mechanical engineers today are pursuing developments in fields such as composites, mechatronics and nano technology.<br>
            Mechanical Engineering overlaps with aerospace engineering, civil engineering, electrical engineering, petroleum engineering and chemical engineering to varying amounts. <br>
            Like other career paths, entry into this particular branch of engineering also requires certain basic traits. First and foremost, you must have a penchant for mechanical components and machinery and how these machines work.<br>
            You must be good at physics, chemistry and mathematics. You must also have skills such as an analytical bent of mind, logical reasoning and problem solving<br>
 

<h1>What does an Mechanical engineer do?</h1><br>
 
Mechanical Engineers are required in all manufacturing facilities.<br>
The working criteria of a mechanical engineer changes according to the type and domain of the company they are working with and the area of specialization.<br>
In a broader sense it can be said that mechanical engineer works on design and control of a system that goes into the process of manufacturing the machinery and product. <br>
He tests new systems for feasibility and efficiency and carries out quality management and improvement process.<br>
<br>

