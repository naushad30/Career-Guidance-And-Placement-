            <h1>Bio technology</h1><br><br>
            Biotechnology means any technological application that uses biological systems or living organisms to make or modify products or processes for specific use. <br>
            It refers to the use of microorganisms such as bacteria, yeasts, or biological substances such as enzymes, to perform specific industrial or manufacturing processes.<br> 
            Applications include production of certain drugs, synthetic hormones, and bulk foodstuffs as well as bioconversion of organic waste. Bio- Technology is concerned with variety of subjects including Biochemistry, Genetics, Microbiology, Chemistry and Engineering.<br>
            Biotechnology has wide areas of application in Agriculture, Animal Husbandry, development of Medicines and Vaccine, treatment of prolonged disease, pollution control and waste management and ecological conservation, energy production and conservation and many more.<br>
            <br><br>
<h1>What does an Bio technology graduate do?</h1><br>
 
•	Bio-Tech Engineers involve in medical processes such as getting organisms to produce new drugs, or using stem cells to regenerate damaged human tissues and re-grow entire organs.<br>
•	Biotechnologist involves industrial processes such as the production of new chemicals or the development of new preservative that can be used for food storage.<br>
•	Bio- Tech Engineers deal with medical devices, instruments used in hospitals & medical labs.<br>
•	Bio Tech Engineers also deal with bioinformatics which involves the use applied mathematics, informatics, statistics, computer science, artificial intelligence, chemistry, and biochemistry<>br
•	Biotechnology experts develop a hybrid plants and involves such processes as the development of pest-resistant grains or the accelerated evolution of disease-resistant animals.<br>

